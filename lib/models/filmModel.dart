class FilmItem {
  final String id;
  final String cover;
  final String title;
  final String description;
  final String genre;
  final String time;
  final String creator;

  FilmItem({
    required String this.id,
    required String this.cover,
    required String this.title,
    required String this.description,
    required String this.genre,
    required String this.time,
    required String this.creator,
  });
}